/* Método main: programas feitos em java tem obrigatoriamente um método main
e é a partir dele que o restante do código é executado.

Sentença: Em Java casa sentença ou linha de código deve obrigatoriamente terminar com ponto e vírgula,
também cada bloco de código deve ser iniciado com o símbolo de chaves e finalizado com o mesmo também

Pacote: a primeira linha deste código diz respeito ao pacote onde este arquivo Java se encontra, de modo geral
são diretórios que permitem organizar o código de forma lógica.
 */

public class Main {
    public static void main(String[] args) {
        for (int p = 0; p < 5; p++) {
            System.out.println(p);
        }
    }
}
